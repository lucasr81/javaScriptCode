/* O JavaScript possui tanto operadores binários quanto unários e um operador ternário, o operador condicional.
 Um operador binário exige dois operandos, um antes do operador e outro depois: */

 //operador binário: {operando1 operador operando2}
 let soma = 2 + 3;
 console.log(soma);

 
let numero = 1;
numero++            // operador unário: {operando operaro}
console.log(numero ); 

//Operadores de atribuição
/*
Um operador de atribuição atribui um valor ao operando à sua esquerda baseado no valor do operando à direita.
 O operador de atribuição básico é o igual (=), que atribui o valor do operando à direita ao operando à esquerda. 
 Isto é, x = y atribui o valor de y a x 
*/
/* 
Nome	     Operador 	Significado
Atribuição	 x = y	    x = y 
*/
let atribuicao = 10;
console.log(atribuicao)
/* 
Nome	     Operador 	Significado
Atribuição	 x += y	    x = x + y 
*/
atribuicao += 10;
//atribuicao = atribuicao + 10;
console.log(atribuicao)

let subtracao = 20
console.log(subtracao)
/* 
Nome	     Operador 	Significado
Atribuição	 x += y	    x = x + y 
*/
subtracao -= 20;
//atribuicao = atribuicao + 10;
console.log(subtracao)